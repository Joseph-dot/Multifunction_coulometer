void NVIC_Init(void)
{
	__HAL_GPIO_EXTI_CLEAR_IT(EXTI1_IRQn);
	HAL_NVIC_SetPriority(EXTI1_IRQn, 3, 0);
  HAL_NVIC_EnableIRQ(EXTI1_IRQn);
}




void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin)
{
	if(GPIO_Pin == Touch_INT_Pin)
	{
		led_on();
		cst026.touch_state |= TP_COORD_UD;
	  cst026_scan(0);
		sprintf((char *)cst026.touch_data,"touch_x: %d touch_y : %d \r\n",cst026.touch_x,cst026.touch_y);
		HAL_UART_Transmit(&huart1,cst026.touch_data,30,10);
	}
}


